package org.teamRITO;

/*
 * This class provides mappings between gamepad buttons and labels to use those buttons.
 * Each button on the gamepad is mapped to an integer within the code, and this maps the labels to those integers.
 */
public class GamepadButtons {

    // GamepadButtons X - Y
    public static final int X_BUTTON = 1;
    public static final int A_BUTTON = 2;
    public static final int B_BUTTON = 3;
    public static final int Y_BUTTON = 4;

}
